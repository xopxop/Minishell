#include "../../includes/minishell.h"

void	env_cmd(char **tokens)
{
	int i;

	(void)tokens;
	i = -1;
	while (g_env[++i])
	{
		ft_putstr(g_env[i]);
		ft_putchar('\n');
	}
}
